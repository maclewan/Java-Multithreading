public class Animal {
    private int xCoord, yCoord;

    public Animal(){
        this.xCoord=0;
        this.xCoord=0;
    }



    public int getxCoord() {
        return xCoord;
    }

    public int getyCoord() {
        return yCoord;
    }

    public void setxCoord(int xCoord) {
        this.xCoord = xCoord;
    }

    public void setyCoord(int yCoord) {
        this.yCoord = yCoord;
    }

}
